import { z } from 'zod';

const subCategorySchema = z.object({
    id: z.string().optional(), // Optional because it will be generated by the database
    subTitle: z.string().min(1, "Subtitle is required"),
    subAmount: z.number().int().min(0, "Sub amount must be a non-negative integer"),
    regularDeliveryDays: z.number().int().min(0, "Regular delivery days must be a non-negative integer"),
    fastDeliveryDays: z.number().int().min(0, "Fast delivery days must be a non-negative integer"),
    fastDeliveryPrice: z.number().int().min(0, "Fast delivery price must be a non-negative integer"),
    categoryId: z.string().min(1, "Category ID is required").optional(), // Optional if not assigning immediately
});


const categorySchema = z.object({
    id: z.string().optional(), // Optional because it will be generated by the database
    categoryName: z.string().min(1, "Category name is required"),
    image: z.string().url("Image must be a valid URL"),
    bulletPoint: z.array(z.string()).optional(),
    requirements: z.array(z.string()).optional(),
    subCategory: z.array(subCategorySchema).optional(), // Optional if not assigning immediately
});

export { categorySchema, subCategorySchema }